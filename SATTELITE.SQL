CREATE TABLE SATELLITE(
SATELLITEID INT PRIMARY KEY,
SATTELITENAME VARCHAR(255) NOT NULL,
LAUNCHDATE DATE,
LAUNCHVEHICLE VARCHAR(100),
ORBITTYPE VARCHAR(50),
MASSKG DECIMAL(15,2),
MANUFACTURER VARCHAR(255),
COUNTRY VARCHAR(50),
OPERATOR VARCHAR(100),
PURPOSE TEXT,
APOGEEKM DECIMAL(15,2),
PERIGMM DECIMAL(15,2),
ECCENTIRICITY DECIMAL(5,4),
INCLINATIONDEGREES DECIMAL(8,4),
PERIODMINUTES DECIMAL(10,2),
TRANSPONDERFREQUENCY VARCHAR(50),
POWERSOURCE VARCHAR(50),
OPERATIONALSTATUS VARCHAR(50),
DATEOUTOFSERVICE DATE,
NOTES TEXT);

